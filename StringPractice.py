# This file will take a string and show various methods to manipulate the characters

myString = "This is my string"

# Print string, basic
print(myString)

# Print upper and lower case of myString
print(myString.upper())
print(myString.lower())

# Printng with different justifications
print("Left Justify:")
print(myString.ljust(30, '-'))
print("Right Justify:")
print(myString.rjust(30, '-'))
print("Center Justify:")
print(myString.center(30, '='))



